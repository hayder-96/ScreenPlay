package com.screenplay.myscreenplay;

public class Itemexample {

    private String example_text;
    private int photo_example;

    public Itemexample(String example_text, int photo_example) {
        this.example_text = example_text;
        this.photo_example = photo_example;
    }

    public String getExample_text() {
        return example_text;
    }

    public int getPhoto_example() {
        return photo_example;
    }
}
